#include <iostream>
#include <cmath> // Для использования sqrt
using namespace std;
int main()
{
    // Оголошення змінних: обираю тип double, щоб працювати з дійсними числами
    double S, D, L;
    const double PI = 3.14; // Константа для значення числа Пі
    // Введення змінної, яку повинен ввести користувач
    cout << "Введіть площу круга (S): ";
    cin >> S;
    // Перевірка введених даних: площа повинна бути додатнім числом
    if (S <= 0) {
        cout << "Помилка: площа повинна бути додатнім числом." << endl;
        return 1; // Завершуємо програму, якщо введено некоректні дані
    }
    // Тут написаний код, за яким будуть обчислюватися значення
       // Обчислення діаметра
    D = sqrt((4 * S) / PI);
    // Обчислення довжини кола
    L = PI * D;
    // Виведення результатів
    cout << "Діаметр круга (D): " << D << endl;
    cout << "Довжина кола (L): " << L << endl;
    return 0; // Завершення програми
}
