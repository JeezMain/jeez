#include <iostream>
#include <cmath>        // Для використання математичних функцій
#include <limits>       // Для обробки помилок введення

using namespace std;

// --- Завдання 1: Обчислення площі прямокутника ---
double RectS(double x1, double y1, double x2, double y2) {
    // Перевірка діапазону координат
    if (x1 < -100 || x1 > 100 || y1 < -100 || y1 > 100 || 
        x2 < -100 || x2 > 100 || y2 < -100 || y2 > 100) {
        cout << "Помилка: координати мають бути в діапазоні [-100, 100]!\n";
        return -1; // Повертаємо -1 як індикатор помилки
    }
    // Обчислення ширини, висоти та площі
    double width = abs(x2 - x1);
    double height = abs(y2 - y1);
    return width * height; // Повертаємо площу
}

// --- Завдання 2: Перевірка, чи число знаходиться в інтервалі ---
bool isBetween(int A, int B, int C) {
    // Перевірка діапазону чисел
    if (A < -100 || A > 100 || B < -100 || B > 100 || C < -100 || C > 100) {
        cout << "Помилка: значення мають бути в діапазоні [-100, 100]!\n";
        return false; // Повертаємо false у разі помилки
    }
    // Перевірка умови: чи число B знаходиться між A і C
    return (A < B && B < C) || (C < B && B < A);
}

// --- Завдання 3: Виведення цифр трицифрового числа ---
void extractDigits(int N) {
    // Перевірка, чи число є трицифровим
    if (N < 100 || N > 999) {
        cout << "Помилка: число має бути трицифровим!\n";
        return; // Вихід із функції у разі помилки
    }
    // Витягування останньої і середньої цифри
    int lastDigit = N % 10;           // Остання цифра
    int middleDigit = (N / 10) % 10;  // Середня цифра
    cout << "Остання цифра: " << lastDigit << endl;
    cout << "Середня цифра: " << middleDigit << endl;
}

// --- Основна функція: Меню для вибору завдань ---
int main() {
    int choice; // Змінна для вибору пункту меню

    do {
        // Виведення меню
        cout << "\nОберіть завдання для виконання:\n";
        cout << "1. Завдання 1: Площа прямокутника\n";
        cout << "2. Завдання 2: Число в інтервалі\n";
        cout << "3. Завдання 3: Цифри трицифрового числа\n";
        cout << "0. Вихід\n";
        cout << "Ваш вибір: ";
        cin >> choice;

        switch (choice) {
            // Завдання 1
            case 1: {
                cout << "Введіть координати (x1, y1, x2, y2): ";
                double x1, y1, x2, y2;
                cin >> x1 >> y1 >> x2 >> y2;
                double area = RectS(x1, y1, x2, y2);
                if (area != -1) { // Перевірка на помилку
                    cout << "Площа: " << area << endl;
                }
                break;
            }
            // Завдання 2
            case 2: {
                cout << "Введіть числа A, B, C: ";
                int A, B, C;
                cin >> A >> B >> C;
                cout << "Результат: " << (isBetween(A, B, C) ? "true" : "false") << endl;
                break;
            }
            // Завдання 3
            case 3: {
                cout << "Введіть трицифрове число: ";
                int N;
                cin >> N;
                extractDigits(N);
                break;
            }
            // Вихід
            case 0:
                cout << "Вихід з програми.\n";
                break;
            default:
                cout << "Некоректний вибір, спробуйте ще раз.\n";
        }
    } while (choice != 0); // Програма працює, поки не вибрано вихід (0)

    return 0; // Завершення програми
}
